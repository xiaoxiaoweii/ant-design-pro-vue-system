            d.time[1] = moment(d.end_date)
            d.planned_arrival_date = moment(d.planned_arrival_date)
            d.editable = true
            d.isNew = true
            d.no = i+1
            d.key =d.id+''
            return d
          })
        })
        getAttachments({id_in_module: data.id, module:modules.actualCost}).then(res => {
          console.log(res)
          this.fileList = res.responseList.map(attachment => {
            attachment.uid = attachment.id.toString()
            attachment.status = 'done'
            attachment.url = ''
            attachment.key = attachment.id.toString()
            attachment.name = attachment.file_name
            attachment.size = attachment.file_size
            attachment.upload_user_id = attachment.upload_user_id.toString()
            attachment.file_size = attachment.file_size.toString()
            attachment.username = attachment.upload_user_name
            return attachment
          })
        })
        this.detailData = this.detailData.map((item, index) => {
          item.code = index
          return item
        })
        const formData = pick(data, ['manage_code','create_date', 'creator_user_name', 'creator_org_name', 'serial_number','name','specification','num','unit','price_without_tax','tax_fee','tax_rate','price_with_tax','sum_with_tax','planned_arrival_date','remark'])
        formData.create_date = moment(data.create_date)
